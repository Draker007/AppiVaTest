package com.example.appiva;

public class list {

    public String list , items;

    public list(String list, String items) {
        this.list = list;
        this.items = items;
    }

    public String getList() {
        return list;
    }

    public void setList(String list) {
        this.list = list;
    }

    public String getItems() {
        return items;
    }

    public void setItems(String items) {
        this.items = items;
    }
}
